    plt.title('Effect of p-value on Token Entropy')
    plt.xlabel('p-value')
    plt.ylabel('Token Entropy')

    # Plot temperature effects
    plt.subplot(2, 2, 3)
    temp_df = diversity_df[diversity_df['p_value'] == 0.85]
